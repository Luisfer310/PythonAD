# Importar la libreria random y usar la funcion random() para dar numeros random 10 veces. Para indicar que queremos 10
# numeros random, usaremos un ciclo for

# Importacion de la libreria
import random
# Loop for con funcion range para crear las veces que se repetira el loop
for n in range(10):
    x = random.random()
    print(x)
