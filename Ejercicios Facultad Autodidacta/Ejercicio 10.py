# Invertir una cadena
cadena = 'Hola mundo'
inversa = cadena[::-1] # Para invertir la cadena usamos algo llamado slising, lo cual es usar el indice, pero solo pasar el 3er argumento, en este se indica cuantas letras 
# imprime y al darle un valor negativo ira del ultimo al primero
print(f'{cadena} - {inversa}')