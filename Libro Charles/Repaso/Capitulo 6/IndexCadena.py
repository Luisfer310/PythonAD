# Podemos acceder a un elemento de una cadena con el indice '[]'
palabra = 'Hola mundo'  # El indice de las cadenas empiezan desde 0, por lo cual la 'H' tendra como indice el numero 0
print(palabra[3])  # Al indicar en el operador de indice que quiero acceder al indice 3, me retornara la letra 'a'

